class Solution
{
    public:
    //Function to remove duplicates from unsorted linked list.
    Node * removeDuplicates( Node *head) 
    {
     unordered_map<int, int> mp;
        Node*prev = NULL;
        Node*curr = head;
        
        while(curr)
        {
            if(mp[curr->data] == 0)
            {
                mp[curr->data] = 1;
                prev = curr;
                curr = curr->next;
            }
            else 
            {
                prev->next = curr->next;
                curr = curr->next;
            }
        }
        return head;
    }
};



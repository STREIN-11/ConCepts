class Solution {
  private:
        int Iterations = 0;
        int k;
        int ans =-1;
    
    public:
        void search(Node* bst)
        {
            if(bst)
            {
                search(bst->left);
                
                Iterations++;
                
                if(Iterations == k)
                    ans = bst->data;
                else if(Iterations > k)
                    return ;
                    
                search(bst->right);
            }
        }
     int KthSmallestElement(Node *bst, int K)
        {
            k = K;
            
            search(bst);
            
            return ans;
        }
};

